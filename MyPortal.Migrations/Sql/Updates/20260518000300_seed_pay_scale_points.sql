-- ============================================================================
-- Seed pay zones, pay scale points, and starter statutory rates.
--
-- Scope:
--   * Zones — all four English bands.
--   * Points — MPS (M1-M6), UPS (U1-U3), Leadership (L1-L18), Unqualified
--     (UNQ1-UNQ6). Leadership L19-L43 + Soulbury/NJC structures are not
--     seeded; load them when needed.
--   * Rates — MPS + UPS only, EffectiveFrom 2024-09-01, taken from the
--     2024-25 STPCD rates published Sept 2024 (England). Leadership and
--     Unqualified rates are intentionally left unseeded so schools verify
--     the values before relying on them.
--
-- WARNING: Salary figures are best-effort and must be verified against the
-- current STPCD document before any payroll-facing use. Annual revaluation
-- is handled by closing existing rows (EffectiveTo = day before new rates
-- take effect) and inserting new ones — never UPDATE AnnualSalary in place.
-- ============================================================================

-- PayZones -------------------------------------------------------------------
