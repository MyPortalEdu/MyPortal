    USING (VALUES
    ('A1B2C3D4-0002-4000-8000-000000000001', 'Inner London',     'INNER'),
    ('A1B2C3D4-0002-4000-8000-000000000002', 'Outer London',     'OUTER'),
    ('A1B2C3D4-0002-4000-8000-000000000003', 'London Fringe',    'FRINGE'),
    ('A1B2C3D4-0002-4000-8000-000000000004', 'Rest of England',  'REST')
    )
    AS Source (Id, Description, Code)
    ON Target.Id = Source.Id

    WHEN NOT MATCHED THEN
    INSERT (Id, Description, Code, Active)
    VALUES (Id, Description, Code, 1)

    WHEN MATCHED THEN
    UPDATE SET Description = Source.Description,
               Code = Source.Code;
